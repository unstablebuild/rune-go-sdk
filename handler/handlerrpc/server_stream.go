// Handler adds Close to a floating handler.
type Handler interface {
	tui.Handler
	io.Closer
}

// StreamMessage abstracts a generated protobuf message
// to enable generic usage of ServerStream and ClientStream.
//
// All the getters can be automatically generated by importing
// the following types and adding them as fields:
//
//	message MyMessage {
//	  handler.MessageType type = ?;
//	  handler.DrawStreamResponse draw = ?;
//	  handler.HandleStreamResponse handle = ?;
//	  handler.CloseStreamResponse close = ?;
//	  handler.CursorStreamResponse cursor = ?;
//	  handler.SelectionStreamResponse selection = ?;
//	  handler.DimensionsStreamResponse dimensions = ?;
//	  handler.ResizeStreamResponse resize = ?;
//
// The setters must be added manually.
type StreamMessage interface {
	ProtoMessage()

	GetType() MessageType
	GetDraw() *DrawStreamResponse
	GetHandle() *HandleStreamResponse
	GetClose() *CloseStreamResponse
	GetCursor() *CursorStreamResponse
	GetSelection() *SelectionStreamResponse
	GetDimensions() *DimensionsStreamResponse

	SetDraw(*DrawStreamResponse)
	SetHandle(*HandleStreamResponse)
	SetClose(*CloseStreamResponse)
	SetCursor(*CursorStreamResponse)
	SetSelection(*SelectionStreamResponse)
	SetDimensions(*DimensionsStreamResponse)
}

// ServerStream implements a tui.Handler (+handler.Floating) server over
// a grpc.ClientStream.
type ServerStream[T StreamMessage] struct {
	log     *slog.Logger
	stream  grpc.ClientStream
	handler Handler
	newT    func() T
	height  atomic.Int32
	width   atomic.Int32
}

// NewServerStream allocates storage for a new Handler server
// and initializes it with the given grpc stream, windowID
// and type parameter constructor.
func NewServerStream[T StreamMessage](
	stream grpc.ClientStream, handler Handler,
	newT func() T,
) *ServerStream[T] {
	logger := slog.Default().With("struct", "handlerrpc.ServerStream")
	return &ServerStream[T]{
		log:     logger,
		stream:  stream,
		handler: handler,
		newT:    newT,
	}
}

// ReceiveMessages receives messages over the grpc stream
// and responds accordingly.
func (c *ServerStream[T]) ReceiveMessages() {
	defer func() {
		// ensure that client doesn't block in case of panic
		err := c.stream.CloseSend()
		c.log.Debug("done streaming, closed connection send", "error", err)
	}()
	c.log.Debug("streaming messages")
	for {
		var recvMsg ServerMessage
		err := c.stream.RecvMsg(&recvMsg)
		if err != nil {
			if strings.Contains(err.Error(), io.EOF.Error()) ||
				status.Code(err) == codes.Canceled {
				c.log.Debug("stream is closing", "reason", err)
			} else {
				c.log.Error("stream receive", "error", err)
			}
			return
		}

		c.log.Debug("receive", "message", &recvMsg)

		sendMsg := c.newT()
		switch recvMsg.GetType() {
		case MessageType_Draw:
			ctx := context.Background()
			var resp DrawStreamResponse
			width := int(c.width.Load())
			height := int(c.height.Load())
			w := newDrawResponseWriter(ctx, width, height)
			c.handler.Draw(w)
			resp.Rows = w.rows

			sendMsg.SetDraw(&resp)
			err = c.stream.SendMsg(sendMsg)

		case MessageType_Resize:
			resize := recvMsg.GetResize()
			width := int(resize.GetWidth())
			height := int(resize.GetHeight())
			c.width.Store(int32(width))
			c.height.Store(int32(height))
			c.handler.Resize(width, height)

		case MessageType_Handle:
			rpcEv := recvMsg.GetHandle().GetEvent()
			var ev term.Event
			ev, err = rpcEv.ToModel()
			if err == nil {
				exit, handled := c.handler.Handle(ev)
				var resp HandleStreamResponse
				resp.Handled = handled
				resp.Quit = exit
				resp.Request = rpcEv
				sendMsg.SetHandle(&resp)
				err = c.stream.SendMsg(sendMsg)
			}

		case MessageType_Cursor:
			coordinates, style, show := c.handler.Cursor()
			var resp CursorStreamResponse
			var respCoordinates termrpc.Coordinates
			respCoordinates.FromModel(coordinates)
			resp.Position = &respCoordinates
			resp.Style = int32(style)
			resp.Show = show
			sendMsg.SetCursor(&resp)
			err = c.stream.SendMsg(sendMsg)

		case MessageType_Selection:
			selection, ok := c.handler.Selection()
			if !utf8.ValidString(selection) {
				c.log.Debug("omit selection from response: not valid utf-8")
				selection = ""
				ok = false
			}
			var resp SelectionStreamResponse
			resp.Text = selection
			resp.Ok = ok
			sendMsg.SetSelection(&resp)
			err = c.stream.SendMsg(sendMsg)

		case MessageType_Dimensions:
			h, ok := c.handler.(handler.Floating)
			if ok {
				width, height := h.Dimensions()
				var resp DimensionsStreamResponse
				resp.Width = int32(width)
				resp.Height = int32(height)
				sendMsg.SetDimensions(&resp)
				err = c.stream.SendMsg(sendMsg)
			}

		case MessageType_Close:
			err := c.handler.Close()
			if err != nil {
				c.log.Error("close handler", "error", err)
			}
			var resp CloseStreamResponse
			sendMsg.SetClose(&resp)
			err = c.stream.SendMsg(sendMsg)
			if err != nil {
				c.log.Error("send close stream response", "error", err)
			}
			c.log.Debug("received close and sent response message")
			return

		case MessageType_Response, MessageType_Request:
			err = errors.New("stream received invalid message: req/resp")
		}
		if err != nil {
			if !errors.Is(err, io.EOF) && !strings.Contains(err.Error(), "context canceled") {
				c.log.Error("stream", "error", err.Error())
			} else if errors.Is(err, io.EOF) {
				// io.EOF indicates that stream status should be discovered
				// through RecvMsg. See SendMsg documentation.
				err = c.stream.RecvMsg(&recvMsg)
				if !strings.Contains(err.Error(), "context canceled") {
					c.log.Error("stream EOF", "error", err)
				} else {
					c.log.Debug("stream EOF", "error", err)
				}
			}
			return
		}
	}
}
